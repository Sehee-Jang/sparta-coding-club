// 반복문
for (let i = 0; i < 10; i++) {
    console.log(i); // 0부터 9까지 출력
}

console.log("------------------------------");

// 2부터 9까지 출력
for (let i = 2; i < 10; i++) {
    console.log(i);
}

console.log("------------------------------");

// 1, 3, 5, 7, 9 출력
for (let i = 1; i < 10; i += 2) {
    console.log(i);
}

console.log("------------------------------");

// 0, 5, 10, 15, ... 100
for (let i = 0; i <= 100; i += 5) {
    console.log(i);
}