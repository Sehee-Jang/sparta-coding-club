// 불리언(Boolean)
// true or false
let bool1 = true;
let bool2 = false;

console.log(bool1);
console.log(typeof bool1);
console.log(bool2);
console.log(typeof bool2);

// Undefined
let x;
console.log(x);

// null 값이 존재하지 않음을 '명시적'으로 나타내는 방법
let y = null;
console.log(y);

// 객체(Object): key-value pair
let person = {
    name: 'choi',
    age: 20,
    isMarried: true,
}

console.log(typeof person);

// 배열(Array)
// 여러 개의 데이터를 순서대로 저장하는 데이터 타입
let number = [1, 2, 3, 4, 5];
let fruit = ['apple', 'banana', 'orange'];